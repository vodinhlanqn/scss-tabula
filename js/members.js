function Members(_id, _account, _fullName, _password, _email, _userType, _language, _description, _avatar) {
    this.id = _id;
    this.account = _account;
    this.fullName = _fullName;
    this.password = _password;
    this.email = _email;
    this.userType = _userType;
    this.language = _language;
    this.description = _description;
    this.avatar = _avatar;
}